import os
import sys
import datetime
import shutil
import ast
import traceback

class PhoenixQArchitect:
    def __init__(self):
        self.name = "PHOENIX-Q [Architect]"
        self.root_path = "C:/Users/loves/workflow"
        self.focus_zones = ["QUANTUM_CORE", "backend", "PHOENIX_CIVITAS"]
        self.creation_log = os.path.join(self.root_path, "data/build_history.log")
        
        print(f"[{self.name}] 제국 건설 지능이 활성화되었습니다.")
        print(f"[{self.name}] 현재 관할 구역: {self.root_path}")

    # ---------------------------------------------------------
    # [설명] 시스템 무결성 검사 및 파일 트리 분석 지식
    # ---------------------------------------------------------
    def scan_empire_structure(self):
        """제국 루트 폴더 내의 모든 디렉토리와 파일의 생존 여부를 확인합니다."""
        print(f"🔍 [{self.name}] 제국 영토 스캔 중...")
        structure = {}
        for root, dirs, files in os.walk(self.root_path):
            level = root.replace(self.root_path, '').count(os.sep)
            indent = ' ' * 4 * (level)
            folder_name = os.path.basename(root)
            print(f"{indent}📂 {folder_name}/")
            structure[folder_name] = files
        return structure

    # ---------------------------------------------------------
    # [요청] 자율 코딩 및 신규 모듈 건설 기능
    # ---------------------------------------------------------
    def build_module(self, target_dir, filename, code_type="python"):
        """
        사령관의 명령에 따라 새로운 기능(Python, HTML, JS)을 자동으로 코딩합니다.
        기존 파일이 있을 경우 .bak 파일로 자동 백업 후 덮어씁니다.
        """
        full_path = os.path.join(self.root_path, target_dir, filename)
        
        # [백업 로직]
        if os.path.exists(full_path):
            timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
            backup_path = f"{full_path}.{timestamp}.bak"
            shutil.copy2(full_path, backup_path)
            print(f"⚠️ [{self.name}] 기존 파일을 백업했습니다: {backup_path}")

        # [샘플 코드 생성 - 실제 주입 시 이 부분을 AI가 생성한 코드로 대체 가능]
        template = ""
        if code_type == "python":
            template = "# Generated by PHOENIX-Q [Architect]\nimport os\n\ndef init_module():\n    pass\n"
        elif code_type == "html":
            template = "<!DOCTYPE html>\n<html>\n<head><title>Phoenix Empire</title></head>\n<body></body>\n</html>"

        try:
            with open(full_path, "w", encoding="utf-8") as f:
                f.write(template)
            print(f"✅ [{self.name}] 신규 모듈 건설 완료: {full_path}")
            self._log_activity(f"Created {filename} in {target_dir}")
        except Exception as e:
            print(f"❌ [{self.name}] 건설 오류 발생: {e}")

    # ---------------------------------------------------------
    # [요청] 코드 최적화 및 자가 리팩토링 지능
    # ---------------------------------------------------------
    def optimize_code(self, file_path):
        """특정 파일의 코드를 읽어 구문 오류를 검사하고 최적화 가능성을 탐색합니다."""
        print(f"⚙️ [{self.name}] {file_path} 코드 최적화 분석 시작...")
        try:
            with open(file_path, "r", encoding="utf-8") as f:
                source = f.read()
            
            # 추상 구문 트리(AST) 분석을 통한 구문 검사
            ast.parse(source)
            print(f"✨ [{self.name}] {file_path}의 코드 무결성이 확인되었습니다.")
            return True
        except SyntaxError as e:
            print(f"🚨 [{self.name}] {file_path}에서 구문 오류 발견: {e}")
            return False

    def _log_activity(self, message):
        """모든 건설 활동을 제국 기록관에게 전달합니다."""
        timestamp = datetime.datetime.now().strftime("[%Y-%m-%d %H:%M:%S]")
        with open(self.creation_log, "a", encoding="utf-8") as f:
            f.write(f"{timestamp} {message}\n")

